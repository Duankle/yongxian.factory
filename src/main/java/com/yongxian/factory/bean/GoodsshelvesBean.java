package com.yongxian.factory.bean;

public class GoodsshelvesBean {
    private Integer id;
    private String shelves_number;
    private String machine_number;
    private String bin;
    private String ip_address;
    private int start_ip;
    private int end_ip;
    private String remark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getShelves_number() {
        return shelves_number;
    }

    public void setShelves_number(String shelves_number) {
        this.shelves_number = shelves_number;
    }

    public String getMachine_number() {
        return machine_number;
    }

    public void setMachine_number(String machine_number) {
        this.machine_number = machine_number;
    }

    public String getBin() {
        return bin;
    }

    public void setBin(String bin) {
        this.bin = bin;
    }

    public String getIp_address() {
        return ip_address;
    }

    public void setIp_address(String ip_address) {
        this.ip_address = ip_address;
    }

    public int getStart_ip() {
        return start_ip;
    }

    public void setStart_ip(int start_ip) {
        this.start_ip = start_ip;
    }

    public int getEnd_ip() {
        return end_ip;
    }

    public void setEnd_ip(int end_ip) {
        this.end_ip = end_ip;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
